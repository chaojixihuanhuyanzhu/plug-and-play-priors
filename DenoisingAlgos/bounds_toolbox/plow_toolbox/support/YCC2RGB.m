function z = YCC2RGB(Y);

[N M ch num] = size(Y);
z = zeros(N, M, 3, num);

for i = 1 : num
    z(:,:,1,i) = Y(:,:,1) - 0.00092460 * Y(:,:,3) + 1.40168676 * Y(:,:,2);
    z(:,:,2,i) = Y(:,:,1) - 0.34369538 * Y(:,:,3) - 0.71416904 * Y(:,:,2);
    z(:,:,3,i) = Y(:,:,1) + 1.77216042 * Y(:,:,3) + 0.00099022 * Y(:,:,2);
end